export const getLanguagesByCategory = (category: string) => 
  PROGRAMMING_LANGUAGES.filter(lang => lang.category === category);

// Search languages
export const searchLanguages = (query: string) => {
  const lowerQuery = query.toLowerCase();
  return PROGRAMMING_LANGUAGES.filter(lang => 
    lang.value.toLowerCase().includes(lowerQuery) ||
    lang.category.toLowerCase().includes(lowerQuery)
  );
};
